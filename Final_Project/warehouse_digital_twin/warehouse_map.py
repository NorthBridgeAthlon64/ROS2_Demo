def grid_to_world(grid_row, grid_col):
    """
    把地图格子坐标转换成世界坐标（米）
    格子(0,0)对应世界坐标(0,0)，在左下角
    """
    x = grid_col * CELL_SIZE + CELL_SIZE / 2  # 格子中心
    y = (MAP_ROWS - 1 - grid_row) * CELL_SIZE + CELL_SIZE / 2  # 翻转Y轴
    return x, y


def world_to_grid(world_x, world_y):
    """
    把世界坐标（米）转换成地图格子坐标
    """
    col = int(world_x / CELL_SIZE)
    row = MAP_ROWS - 1 - int(world_y / CELL_SIZE)
    return row, col


def get_cell(row, col):
    """
    获取某个格子的类型
    返回：0=空地, 1=墙壁, 2=货架, 3=卸货区
    """
    if row < 0 or row >= MAP_ROWS or col < 0 or col >= MAP_COLS:
        return 1  # 超出地图范围，视为墙壁
    return WAREHOUSE_MAP[row][col]


def find_cells_by_type(cell_type):
    """
    找到地图上所有指定类型的格子
    比如找到所有货架的位置
    """
    positions = []
    for row in range(MAP_ROWS):
        for col in range(MAP_COLS):
            if WAREHOUSE_MAP[row][col] == cell_type:
                x, y = grid_to_world(row, col)
                positions.append((x, y))
    return positions
